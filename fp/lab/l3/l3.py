import turtle

def rechteck():
    t = turtle.Pen()
    t.forward(200)
    t.left(90)
    t.forward(100)
    t.left(90)
    t.forward(200)
    t.left(90)
    t.forward(100)
    t.penup()
    t.goto(75, 25)
    t.setheading(0)
    t.pendown()
    t.forward(50)
    t.left(90)
    t.forward(25)
    t.left(90)
    t.forward(50)
    t.left(90)
    t.forward(25)
    turtle.done()

